public class Human {
    int age;
    String name;
    double weight;

    public Human(int age, String name, double weight) {
        this.name = name;
        this.weight = weight;
        this.age = age;
    }

    public int getAge() {
        return this.age;
    }

    public void setAge() {
        this.age = age;
    }

    public String getName() {
        return this.name;
    }

    public void setName() {
        this.name = name;

    }


}


